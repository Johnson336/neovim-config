-- Sets border for other windows, like LspInfo
require('lspconfig.ui.windows').default_options.border = _border

-- set some bufferline options
local bufferline = require('bufferline')
bufferline.setup {
  options = {
    -- Changes BufferLine separators
    separator_style = {'', ''},
    -- Turns off indicators for current buffer
    indicator = {
      style = 'none',
    },
    -- Indents BufferLine when NeoTree is open
    offsets = {
      {
        filetype = "neo-tree",
        text = "File Explorer",
        text_align = "center",
        separator = false,
      },
    },
    close_command = require('bufdelete').bufdelete,
    -- Force close buffer on right click
    right_mouse_command = "Bdelete! %d",
    show_buffer_close_icons = false,
    show_close_icon = false,
    show_buffer_icons = false,
  },
}


-- finally set the colorscheme
vim.cmd.colorscheme('tokyonight')

